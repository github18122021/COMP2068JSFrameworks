const mongoose = require('mongoose');

// User Schema
// username, email, password
const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true
    },
    password: {
        type: String,
        required: true,
        minlength: 6
    }
});

// users collection in the database
module.exports = mongoose.model('User', userSchema);


